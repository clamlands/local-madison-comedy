<h1>Stand-up Shows</h1>
<h2>Cheshire Cat Comedy</h2>
<p>
  Founded by Sasha Rosser and then bringing on producers Noah Mailloux, Josh Glen, and
  Cody Lemke, Cheshire Cat Comedy brings live entertainment to venues across the Madison
  area and beyond. Their shows feature stand-up, funny educational lectures, live music,
  burlesque, drag, and more.
</p>
<h2>Capitol Comedy Hour</h2>
<p>
  Capitol Comedy Hour is a late-night style variety show produced by Matt Jordan and
  Andrew Rynning, featuring the writing, performing, filming, and editing contributions of
  many other comics in the scene.
</p>
<h2>Silly Street Comedy</h2>
<p>
  Founded by Eli Wilz, Silly Street Comedy is a cooperative effort of local comedians in
  Madison to bring great comedy to the Willy Street Neighborhood.
</p>
